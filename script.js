const localStorageKey = 'daily-routine-codes'

function validadeIfExistNewTask()
{
    let values = JSON.parse(localStorage.getItem(localStorageKey) || "[]")
    let inputValue = document.getElementById('input-new-task').value
    let exist = values.find(x => x.name == inputValue)
    return !exist ? false : true
}



function newTask()
{
    let input = document.getElementById('input-new-task')
    input.style.border = ''

    if(!input.value)
    {
        input.style.border = '1px solid red'
        alert('Digite a sua tarefa do dia')
    }

    else if(validadeIfExistNewTask())
    {
        alert('JÃ¡ existe essa tarefa')
    }

    else
    {
        let values = JSON.parse(localStorage.getItem(localStorageKey) || "[]")
        values.push({
            name: input.value
        })
        localStorage.setItem(localStorageKey,JSON.stringify(values))
        showValues()
    }
    input.value = ''
}



